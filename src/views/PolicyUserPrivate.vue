共享物联网智能设备。您可通过手机号邀请好友实现物联网智能设备的共享。当您选择通过手机号邀请好友时，您需要通过手动输入家人或朋友手机号，共享您的物联网智能设备。我们会将您提供的家人或朋友的手机号加密上传到服务器，实现手机号邀请好友共享智能硬件设备。
