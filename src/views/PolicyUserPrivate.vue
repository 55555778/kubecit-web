            一、我们如何收集和使用您的个人信息

我们将按照本政策所述收集和使用您的个人信息，包括您的敏感信息，收集和使用这些敏感信息的目的和用途详见下述功能描述部分。个人敏感信息一旦泄露、非法提供或滥用可能危害人身和财产安全，极易导致个人名誉、身心健康受到损害或歧视行待遇等。因此，我们会采取合规技术对您明确同意和主动提供的敏感信息进行加密传输和存储，保护您个人信息的安全。


我们不会通过共享、转让、搜集公开信息等其他渠道间接获取有关您的个人信息。如将来发生需通过其他渠道间接获取您的个人信息时，我们只获取实现产品或服务的业务功能所必需的最少数量的信息，且在收集和使用之前明确告知您并征求您的明示同意。同时，我们将在收集信息前确保信息来源的合法性，了解个人信息提供方已获得的个人信息处理的授权同意范围，包括使用目的、授权同意转让、共享、公开披露等。若我们开展业务需进行的个人信息处理活动超出该授权同意范围，会在获取个人信息后的合理期限内或处理个人信息前，征得您的明示同意。


我们收集和使用您的信息的目的和用途详情如下：

（一）实现BOTSLAB产品的核心功能

核心功能是指支撑本款产品和APP正常运行和提供服务的基本功能。若您不同意我们收集和使用向您提供服务所必需的个人信息，将无法使用本款产品。

1.        通过注册账号收集的信息。当您在手机上安装本款产品关联的云智连APP后，需要在云智连APP上注册或登录云智连账号方可使用。当您注册云智连帐号时，我们会收集您的手机号码、邮箱、用户名、非明文密码，收集这些信息是为了帮助您完成云智连帐号注册，保护您的帐号安全。如果您拒绝提供这些信息，您可能无法成功注册云智连帐号。您可通过【我的->账号管理】上传或填写、修改您的头像、昵称以及密码。您注销云智连账号时，我们将停止使用并删除上述信息或对您的个人信息进行匿名化处理，法律法规另有规定的除外。

2.        绑定物联网智能设备。在您登录云智连APP后，您可通过【家庭->添加设备】，实现云智连APP与物联网智能设备的连接和绑定。物联网智能设备通过云智连APP操作，需要验证其真实性，因此我们会需要通过核验出厂MAC、设备SN信息、DID信息来确认物联网智能设备的合法身份，获取手持终端设备信息，如网络类型、手机型号、品牌、系统版本号、语言、国家、时区，用于排查产品问题与显示时间。物联网智能设备需要连接网络使用的情况下，需要调取您的位置权限，获取您的WIFI列表，通过云智连APP对其进行相应的网络设置，您录入相应的Wi-Fi名称及密码，该信息仅用于物联网智能设备配网使用。您填写的Wi-Fi及密码数据，我们会加密存储在物联网智能设备本地，不会上传服务器。同时，我们需要通过判断IP、网络信号为设备选择最优联网方式，来保障设备的联网稳定性。

3.        操控物联网智能设备。当您绑定物联网智能设备后，您可通过云智连APP中的【家庭->设备列表】对本款产品的功能服务进行操控。当您操控设备时，我们需要搜集您的设备信息、操控指令信息，用于操控设备。同时，在添加设备环节，请您仔细阅读并同意与物联网智能设备相关的具体隐私保护政策。您设置和提供的信息会上传到我们的服务器，作为对物联网智能设备的指令，实现对物联网智能设备的操控。

4.        设置自动化任务。您物联网智能设备上报的事件信息（信息来自于您添加的设备，具体的可设置信息取决于您对该设备的隐私授权范围，每个添加设备会提供设备提供方的隐私政策相关条款由您选择是否同意），可以用于创建属于您个人的自动化任务。

